package javaCourseWork;

public class Course {
	
	int id;
	String name;
	String teacher;
	String explanation;
	
	public Course() {
		
	}
	public Course(int id, String name, String teacher, String explanation) {
		super();
		this.id = id;
		this.name = name;
		this.teacher = teacher;
		this.explanation = explanation;
	}
	

}
